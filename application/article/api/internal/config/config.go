package config

import "github.com/zeromicro/go-zero/rest"

type Config struct {
	rest.RestConf
	Auth struct {
		AccessSecret string
		AccessExpire int64
	}
	Oss
}

type Oss struct {
	Endpoint         string
	AccessKeyId      string
	AccessKeySecret  string
	BucketName       string
	ConnectTimeout   int64 `json:",optional"`
	ReadWriteTimeout int64 `json:",optional"`
}
